);
CREATE TABLE IF NOT EXISTS "USUARIOS" (
	"CPF"	TEXT NOT NULL UNIQUE,
	"SENHA"	TEXT NOT NULL,
	"CARTAO"	TEXT NOT NULL UNIQUE,
