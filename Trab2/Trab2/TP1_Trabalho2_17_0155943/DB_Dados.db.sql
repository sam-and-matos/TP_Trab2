	"NUMERO_CARTAO"	TEXT NOT NULL UNIQUE,
	"CODIGO_VERIFICADOR"	TEXT,
	"DATA_VALIDADE"	TEXT,
	PRIMARY KEY("NUMERO_CARTAO")
);
